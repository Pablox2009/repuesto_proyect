    private void boton_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_agregarActionPerformed
        codigo = codigoTXT.getText();
        nombre = nombreTXT.getText();
        if(codigo.equals("0") ){
            JOptionPane.showMessageDialog(null, "Seleccione un cliente", "Error", JOptionPane.WARNING_MESSAGE);
        }
        else{
            if(nombre.equals("") || codigo.equals("")){
                JOptionPane.showMessageDialog(null, "Complete los campos", "Error", JOptionPane.WARNING_MESSAGE);
            }
            else{
                cd.agregar(codigo, nombre);
                clientes();
                vaciar();
                codigo = "";
            }
        }
    }//GEN-LAST:event_boton_agregarActionPerformed

    private void boton_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_modificarActionPerformed
        id = cm.getId_cliente();
        codigo = codigoTXT.getText();
        nombre = nombreTXT.getText();
        if(codigo.equals("0") || nombre.equals("")){
            JOptionPane.showMessageDialog(null, "Complete los campos", "Error", JOptionPane.WARNING_MESSAGE);
        }
        else{
            cd.modificar(id, codigo, nombre);
            clientes();
            vaciar();
            codigo = "";
        }
    }//GEN-LAST:event_boton_modificarActionPerformed

    private void boton_eliiminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_eliiminarActionPerformed
        nombre = nombreTXT.getText();
        id = cm.getId_cliente();
        if(id.equals("")){
            JOptionPane.showMessageDialog(null, "Seleccione un cliente", "Error", JOptionPane.WARNING_MESSAGE);
        }
        else{
            int opcion = JOptionPane.showConfirmDialog(null,"Desea eliminar a "+nombre+"?","Confirmar", JOptionPane.YES_NO_OPTION);
            if(opcion==0){
                cd.eliminar(id);
                clientes();
                id = "";
            }
        }
    }//GEN-LAST:event_boton_eliiminarActionPerformed

